def getColor(name):
    if name == "red":
        return [191,90,47]
    elif name == "green":
        return [47,191,90]
    elif name == "blue":
        return [47,90,191]
    elif name == "brown":
        return [104,54,33]
    elif name == "black":
        return [0,0,0]
    elif name == "white":
        return [255,255,255]
    else:
        return None